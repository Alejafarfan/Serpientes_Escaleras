        String players = cmbJugadores.getSelectedItem().toString();

        if ("10x10".equals(modo)) {
            Tablero tabla= new Tablero(modo);
           Es_10x10 juego = new Es_10x10(players);
            juego.setVisible(true);
            this.dispose();
        } else {
            if (modo == "13x13") {
                Tablero tabla = new Tablero(modo);
                Es_13x13 juego = new  Es_13x13(players);
                juego.setVisible(true);
                this.dispose();
            }else {
                Tablero tabla = new Tablero(modo);
                Es_15x15 juego = new Es_15x15(players);
                juego.setVisible(true);
                this.dispose();
            }
        }
